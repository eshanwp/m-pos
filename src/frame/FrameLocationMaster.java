    void idLocation() {

        try {

            ResultSet rs = cls.db.myConnection().createStatement().executeQuery("SELECT MAX(idlocation) FROM location");

            while (rs.next()) {

                if (rs.getInt("MAX(idlocation)") == 0) {
                    
                    jLabelBranchId.setText("1");

                } else {
                    int i = rs.getInt("MAX(idlocation)") + 1;
                    jLabelBranchId.setText(i + "");

                }
            }

        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }

