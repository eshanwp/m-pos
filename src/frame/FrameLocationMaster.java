    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed

        String Status = "ACTIVE";
        if (jTextFieldBranchCode.getText().length() <= 0 || jTextFieldBranchName.getText().length() <= 0) {
            if (jTextFieldBranchCode.getText().length() <= 0) {
                jLabelBranchCode.setText("Required");
            }

            if (jTextFieldBranchName.getText().length() <= 0) {
                jLabelBranchName.setText("Required");
            }

        } else {

            try {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                Vector v = new Vector();
                v.add(jLabelBranchId.getText());
                v.add(jTextFieldBranchName.getText());
                v.add(jTextFieldBranchCode.getText());
                v.add(Status);

                dtm.addRow(v);
                String id = jLabelBranchId.getText();
                int i = Integer.parseInt(id);
                i++;
                jLabelBranchId.setText(i + "");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }

        }


    }//GEN-LAST:event_jButtonAddActionPerformed

