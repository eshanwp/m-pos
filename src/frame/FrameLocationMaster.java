    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed

        AC.jButtonXRight(333, 333, 10, 5, jButtonSearch);
        AC.jButtonXRight(330, 330, 10, 5, jButtonUpdate);

        jButtonSave.setVisible(false);
        jButtonAdd.setVisible(false);

        if (jTextFieldBranchCode.getText().length() <= 0 || jTextFieldBranchName.getText().length() <= 0) {
            if (jTextFieldBranchCode.getText().length() <= 0) {
                jLabelBranchCode.setText("Required");
            }

            if (jTextFieldBranchName.getText().length() <= 0) {
                jLabelBranchName.setText("Required");
            }

        } else {

            AccessPoint ap = new AccessPoint();
            String status = ap.acceesPoint_02(USER_NAME);

            if (status.equals("true")) {

                int s = JOptionPane.showConfirmDialog(null, "Are you sure change.");

                if (s == JOptionPane.OK_OPTION) {

                    Location l = new Location();
                    l.update();
                }

            } else {
                jButtonUpdate.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "This access is not grant you\nCan't update details.");

            }

        }


    }//GEN-LAST:event_jButtonUpdateActionPerformed

