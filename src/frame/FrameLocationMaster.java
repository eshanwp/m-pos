    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        jButtonUpdate.setVisible(true);
        jCheckBoxInactive.setVisible(true);
        AC.jButtonXRight(280, 333, 10, 5, jButtonSearch);
        AC.jButtonXRight(270, 330, 10, 5, jButtonUpdate);
        jButtonSave.setVisible(false);
        jButtonAdd.setVisible(false);

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        try {

            if (jTextFieldBranchCode.getText().length() <= 1 && jTextFieldBranchName.getText().length() <= 1) {

                Location l = new Location();
                l.refresh();

            } else if (jTextFieldBranchCode.getText().length() >= 1 || jTextFieldBranchName.getText().length() >= 1) {

                Location l = new Location();
                l.search();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

