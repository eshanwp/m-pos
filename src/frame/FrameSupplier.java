    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        if (jTextFieldSupplierCode.getText().length() == 0 || jTextFieldSupplierName.getText().length() == 0 || jTextFieldAdd1.getText().length() == 0
                || jTextFieldAdd2.getText().length() == 0 || jTextFieldPhone.getText().length() == 0 || jTextFieldMobile1.getText().length() == 0
                || jTextFieldCompanyName.getText().length() == 0 || jComboBoxCountry.getSelectedIndex() == 0
                || jTextAreaIntials.getText().length() == 0 || jTextFieldLastName.getText().length() == 0) {

            if (jTextFieldSupplierCode.getText().length() == 0) {
                jLabelSupplierCode.setText("Required");
            }

            if (jTextFieldSupplierName.getText().length() == 0) {
                jLabelSupplierName.setText("Required");
            }

            if (jTextFieldAdd1.getText().length() == 0) {
                jLabelAdd1.setText("Required");
            }

            if (jTextFieldAdd2.getText().length() == 0) {
                jLabelAdd2.setText("Required");
            }

            if (jTextFieldPhone.getText().length() == 0) {
                jLabelPhone.setText("Required");
            }

            if (jTextFieldMobile1.getText().length() == 0) {
                jLabelMobile1.setText("Required");
            }

            if (jTextFieldCompanyName.getText().length() == 0) {
                jLabelCompanyName.setText("Required");
            }

            if (jComboBoxCountry.getSelectedIndex() == 0) {
                jLabelCountry.setText("Required");
            }

            if (jTextAreaIntials.getText().length() == 0) {
                jLabelIntials.setText("Required");
            }

            if (jTextFieldLastName.getText().length() == 0) {
                jLabelLastName.setText("Required");
            }

        } else {

            AccessPoint ap = new AccessPoint();
            String status = ap.acceesPoint_01(USER_NAME);

            if (status.equals("true")) {

                int s = JOptionPane.showConfirmDialog(null, "Are you sure, you want to Save?");

                if (s == JOptionPane.OK_OPTION) {

                    Supplier s1 = new Supplier();
                    s1.save(id_country, name_initials);

                    this.dispose();
                    FrameSupplier s2 = new FrameSupplier(LOACATION_ID, USER_NAME);
                    s2.setVisible(true);
                }

            } else {
                jButtonUpdate.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "This access is not grant you\nCan't save details.");

            }

        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

