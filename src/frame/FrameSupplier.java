    private void jTextFieldSupplierNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldSupplierNameFocusLost

        jTextFieldMobile1.grabFocus();

        String s = jTextFieldSupplierName.getText();

        jLabelSupplierName.setText("");

        String[] arr = s.split(" ");
        // print all the initials
        for (int i = 0; i < arr.length - 1; i++) {

            jTextAreaIntials.append(arr[i].charAt(0) + ".");
        }
        // print the last name
        jTextFieldLastName.setText(arr[arr.length - 1]);

        try {

            name_initials = jTextAreaIntials.getText() + jTextFieldLastName.getText();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e + "\nPlease contact the system administrator");
            this.dispose();
        }

    }//GEN-LAST:event_jTextFieldSupplierNameFocusLost

