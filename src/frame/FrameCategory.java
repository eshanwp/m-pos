    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed

        int i = jTable1.getRowCount();

        String s1 = "";
        String s2 = "";
        boolean exists = false;

        if (jTextFieldCategoryCode.getText().length() == 0 || jTextFieldCategoryName.getText().length() == 0) {

            if (jTextFieldCategoryCode.getText().length() == 0) {
                jLabelCategoryCode.setText("Required");
            }

            if (jTextFieldCategoryName.getText().length() == 0) {
                jLabelCategoryName.setText("Required");
            }

        } else {

            for (int j = 0; j < jTable1.getRowCount(); j++) {
                s1 = jTable1.getValueAt(j, 0).toString();
                s2 = jTable1.getValueAt(j, 1).toString();

                if (jTextFieldCategoryCode.getText().equals(s1) || jTextFieldCategoryName.getText().equals(s2)) {
                    exists = true;
                    break;
                }

            }
            
            if (!exists) {
                
                Category c = new Category();
            String Result[] = c.search();

            if (Result[0] != null || Result[1] != null || Result[2] != null) {

                if (Result[1].equals(jTextFieldCategoryCode.getText())) {

                    JOptionPane.showMessageDialog(rootPane, "Category code already used");
                    jTextFieldCategoryName.setText(Result[2]);
                    jTextFieldCategoryName.setEditable(false);

                } else if (Result[2].equals(jTextFieldCategoryName.getText())) {

                    JOptionPane.showMessageDialog(rootPane, "Category name already used");
                    jTextFieldCategoryName.setText(Result[2]);
                    jTextFieldCategoryName.setEditable(false);

                }

            } else {

                dtm = (DefaultTableModel) jTable1.getModel();
                Vector v = new Vector();

                v.add(jTextFieldCategoryCode.getText());
                v.add(jTextFieldCategoryName.getText());
                v.add("ACTIVE");
                v.add("DELETE");
                dtm.addRow(v);

                jTextFieldCategoryCode.setText("");
                jTextFieldCategoryName.setText("");
                
                jTextFieldCategoryCode.grabFocus();

            }
                
                
            } else {
                
                JOptionPane.showMessageDialog(rootPane, "Data already exist.");
            }

        }

    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        dtm = (DefaultTableModel) FrameCategory.jTable1.getModel();

        int i = FrameCategory.jTable1.getSelectedRow();
        int j = FrameCategory.jTable1.getSelectedColumn();

        String s = dtm.getValueAt(i, j).toString();

        String s1 = "DELETE";
        System.out.println(s);
        if (s.equals(s1)) {
            dtm.removeRow(i);
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        int i = jTable1.getRowCount();

        if (i == 0) {
            JOptionPane.showMessageDialog(rootPane, "Please add data");
        } else {

            AccessPoint ap = new AccessPoint();
            String status = ap.acceesPoint_01(USER_NAME);

            if (status.equals("true")) {

                int s = JOptionPane.showConfirmDialog(null, "Are you sure, you want to Save?");

                if (s == JOptionPane.OK_OPTION) {
                    Category c = new Category();
                    c.save();
                    this.dispose();
                    FrameCategory c1 = new FrameCategory(LOACATION_ID, USER_NAME);
                    c1.setVisible(true);
                }

            } else {
                jButtonUpdate.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "This access is not grant you\nCan't save details.");

            }

        }
    }//GEN-LAST:event_jButtonSaveActionPerformed
