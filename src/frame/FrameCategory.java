    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed

        if (jTextFieldCategoryCode.getText().length() == 0 && jTextFieldCategoryName.getText().length() == 0) {

            Category c = new Category();
            c.searchAll();

            jButtonAdd.setEnabled(false);
            jButtonUpdate.setEnabled(false);
            jButtonSave.setEnabled(false);
            jCheckBox.setVisible(true);

            int i = FrameCategory.jTable1.getRowCount();
            jLabel2.setText("NO OF CATEGORY = " + i);

        } else if (jTextFieldCategoryCode.getText().length() > 0 || jTextFieldCategoryName.getText().length() > 0) {

            jTable1.setVisible(false);
            jButtonAdd.setEnabled(false);
            jButtonSave.setEnabled(false);
            jButtonUpdate.setEnabled(true);
            jCheckBox.setVisible(true);

            Category c = new Category();
            String result[] = c.search();

            jTextFieldCategoryName.setText(result[2]);
            jTextFieldCategoryCode.setText(result[1]);
            id_Category = result[0];

        }

    }//GEN-LAST:event_jButtonSearchActionPerformed

