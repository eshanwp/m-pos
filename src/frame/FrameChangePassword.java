    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        String DB_PASSWORD = null;
        try {

            ResultSet rs = cls.db.myConnection().createStatement().executeQuery("SELECT * FROM user WHERE userName='" + jTextFieldUserName.getText() + "' ");
            while (rs.next()) {
                DB_PASSWORD = rs.getString("oldPassword");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        if (DB_PASSWORD.equals(OLD_PASSWORD)) {

            int s = JOptionPane.showConfirmDialog(null, "Are you sure change.");

            if (s == JOptionPane.OK_OPTION) {

                try {

                    cls.db.myConnection().createStatement().executeUpdate("UPDATE user SET password1='" + pw1 + "', password2='" + pw2 + "', oldPassword='" + OLD_PASSWORD + "'  WHERE userName='" + jTextFieldUserName.getText() + "' ");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e);
                }

                JOptionPane.showMessageDialog(rootPane, "Sucessfully Update");
                this.dispose();
                FrameMain mf = new FrameMain();
                System.exit(0);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Old password is wrong");
        }

    }//GEN-LAST:event_jButtonSaveActionPerformed

